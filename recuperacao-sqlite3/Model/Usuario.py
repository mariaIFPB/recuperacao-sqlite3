class Usuario():
    def __init__(self, nome, email, telefone, senha, genero, data_nasc, profissao, cidade):
        self.nome = nome
        self.email = email
        self.telefone = telefone
        self.senha = senha
        self.genero = genero
        self.data_nasc = data_nasc
        self.profissao = profissao
        self.cidade = cidade
